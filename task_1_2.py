# Variablen + Datentypen

# Drei Variablen mit unterschiedlichen Datentypen erstellen
name = "Alice"          # String
alter = 30              # Integer
groesse = 1.65          # Float

# Ausgabe der Variablen und ihrer Datentypen
print(f"Name: {name}, Typ: {type(name)}")
print(f"Alter: {alter}, Typ: {type(alter)}")
print(f"Grösse: {groesse}, Typ: {type(groesse)}")

